#ifndef BIT_OPERATIONS_H
#define BIT_OPERATIONS_H

/**********************************************************************************************************************
 * INCLUDES
 *********************************************************************************************************************/
#include "Platform_Types.h"

/**********************************************************************************************************************
 *  GLOBAL CONSTANT MACROS
 *********************************************************************************************************************/

#define SRAM_BB_ALIAS_BASE 22000000
#define SRAM_BASE 20000000

#define PREIPHRAL_BB_ALIAS_BASE 42000000
#define PREIPHRAL_BASE 40000000

/**********************************************************************************************************************
 *  GLOBAL FUNCTION MACROS
 *********************************************************************************************************************/
#define SET(REG,BIT)	      ((uint32)(REG|=(1<<BIT)))
#define CLR(REG,BIT)	      ((uint32)(REG &=~(1<<BIT)))
#define GET(REG,BIT) 	      ((uint32)((REG>>BIT)&1)))
#define TOGGLE(REG,BIT)     ((uint32)(REG^=(1<<BIT)))
#define Preiph_BB(REG,BIT)  ((uint32)(PREIPHRAL_BB_ALIAS_BASE+((REG-PREIPHRAL_BASE)*32)+(BIT*4)))
#define RAM_BB(REG,BIT)     ((uint32)(SRAM_BB_ALIAS_BASE+((REG-SRAM_BASE)*32)+(BIT*4)))


 

 
#endif  /* BIT_OPERATIONS_H */

